    public void selectData() {
       String kolom[] = {"Kode", "Nama", "Harga","Qty","Deskripsi"};
       DefaultTableModel dtm = new DefaultTableModel(null, kolom);
       String SQL = "SELECT * FROM tb_product";
       ResultSet rs = KoneksiDB.executeQuery(SQL);
       try{
           while(rs.next()){
               String Kode = rs.getString(1);
               String Nama = rs.getString(2);
               String Harga = rs.getString(3);
               String Qty = rs.getString(4);
               String Deskripsi = rs.getString(5);
               String data[] = {Kode,Nama,Harga, Qty, Deskripsi};
               dtm.addRow(data);
               
           }
           
       }catch (SQLException ex){
           Logger.getLogger(dua.class.getName()).log(Level.SEVERE, null, ex);
           
       }
       tabel.setModel(dtm);
    }
