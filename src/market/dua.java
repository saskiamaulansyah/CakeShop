    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
          if("".equals(kode.getText()) || "".equals(produk.getText()) || "".equals(harga.getText()) || "".equals(qty.getText()) || "".equals(des.getText())){
            JOptionPane.showMessageDialog(this, "Dilengkapi yaa!", "Error", JOptionPane.WARNING_MESSAGE);
        } else{
           
           String SQL = "INSERT INTO tb_product " + "VALUES('" + kode.getText()+ "','"+produk.getText()+"','"+harga.getText()+"','"+qty.getText()+"','"+des.getText()+"')";
           int status = KoneksiDB.execute(SQL);
           if(status == 1){
               JOptionPane.showMessageDialog(this, "Produk sudah ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
               selectData();
               
           }else{
               JOptionPane.showMessageDialog(this, "Produk gagal ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
           }
        }
    }//GEN-LAST:event_saveMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        int baris = tabel.getSelectedRow();
       if (baris != -1){
           String kode = tabel.getValueAt(baris, 0).toString();
           String SQL = "DELETE FROM tb_product WHERE Kode ='"+kode+"'";
           int status = KoneksiDB.execute(SQL);
           if(status == 1){
               JOptionPane.showMessageDialog(this, "Produk sudah dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
           } else{
               JOptionPane.showMessageDialog(this, "Produk gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
           }
       }else{
           JOptionPane.showMessageDialog(this, "Pilih baris dongg", "Error", JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_deleteMouseClicked

